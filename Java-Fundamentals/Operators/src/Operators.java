public class Operators {
    public static void main(String[] args) {

        // Arithmetic
        //      + : sum
        //      - : subtract
        //      * : multiplication
        //      / : Division

        // Logical, relational and Boolean
        //      > : Greater than
        //      < : Less than
        //      <> : Greater than or Less than
        //      != : Different than
        //      == : The same than
        //      && : And Logical
        //      || : Or Logical


        // Increment and Decrement
        //      ++ : Increment 1
        //      -- : Decrement 1
        //      +=N : Specific increment
        //      -=N : Specific Decrement


        // Concatenation
        //      + : Joins or concatenates, depending on the context
        //

        // Unitary Operators
        //      -  : Changes the sign of the numerical value
        //      !  : Inverts the Boolean value


        int a = 5;

        int b;

        b = 7;

        double c = b + a;

        c++;

        c+=7;

        c = b * a;

        c = b / a;

        c = (b * a) / 2;

        System.out.print(c);

        int d = -a;     // -5

        boolean e = true;

        boolean f = !e;



    }
}
